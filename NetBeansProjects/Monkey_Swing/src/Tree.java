
public class Tree {

   private String name;
   private int height;

    public Tree(String n, int h) {
        name = n;
        height = h;
    }

    public String getname() {
        return name;
    }

    public int getheight() {

        return height;
    }

    public void setname(String n) {
        name = n;
    }

    public void setheight(int h) {
        height = h;
    }

}
